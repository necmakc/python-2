# Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с повторениями). Выдать без
# повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
# Пользователь вводит 2 числа. n — кол-во элементов первого множества. m — кол-во элементов второго
# множества. Затем пользователь вводит сами элементы множеств.

from random import randint

array_1, array_2 = [], []
unique_array_1, unique_array_2 = set(), set()

length_array_1 = int(
    input("Введите длинну первого набора: "))
length_array_2 = int(
    input("Введите длинну второго набора: "))

for i in range(length_array_1):
    array_1.append(randint(0, 10))
for i in range(length_array_2):
    array_2.append(randint(0, 10))

for i in range(len(array_1)):
    unique_array_1.add(array_1[i])
for i in range(len(array_2)):
    unique_array_2.add(array_2[i])

print(array_1)
print(array_2)
print(unique_array_1.intersection(unique_array_2))

